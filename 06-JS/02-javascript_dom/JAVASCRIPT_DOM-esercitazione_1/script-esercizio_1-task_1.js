/*
- 	document.write(stringa) e document.writeln(stringa) 
	scrive del testo alla fine della pagina (utilizzate codice HTML);
	
- 	alert(stringa) 
	visualizza un popup contenente un messaggio di testo. Viene visualizzato solo un pulsante OK;
	
-	confirm(stringa). 
	visualizza un popup con il messaggio passato per parametro per chiedere una conferma all'utente. 
	Nel caso prema il pulsante "Ok" la chiamata restituisce true, altrimenti false;
	
-	prompt(stringa) 
	visualizza un popup con il messaggio passato per parametro. Viene visualizzato nel popup un text field per 
	inserire una stringa che viene restituita come risultato della chiamata;
*/

// document.write()
document.write("questa è la stampa con document.write");
document.writeln("questa è la stampa con document.writeln");

// alert
alert("questo è un alert");

// confirm
confirm("questo è una confirm");

// prompt
prompt("questo è un propt");
